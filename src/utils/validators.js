/**
 * 验证密码强度
 * 规则：至少 6 个字符，必须包含至少一个字母和一个数字。
 * 可以根据需要修改正则表达式来增强难度，例如要求包含特殊符号。
 */
export function isStrongPassword(password) {
    // 正则解释：
    // ^(?=.*[A-Za-z])  -> 必须包含至少一个字母
    // (?=.*\d)         -> 必须包含至少一个数字
    // (?=.*[@$!%*#?&.]) -> 至少包含一个常见特殊符号
    // [A-Za-z\d@$!%*#?&.]{6,}$ -> 只能包含字母、数字和常见特殊符号，且至少6位
    const regex = /^(?=.*[A-Za-z])(?=.*\d)(?=.*[@$!%*#?&.])[A-Za-z\d@$!%*#?&.]{6,}$/;
    return regex.test(password);
}

export const PASSWORD_RULE_MESSAGE = "密码至少需要 6 个字符，且必须同时包含字母、数字和特殊符号。";